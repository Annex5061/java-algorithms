public class Main {
  public static void main(String[] args) {
    // Adding
    int a = 1;
    int b = 99;
	int c = a + b;
	System.out.println(c);
    
    // Subtracting
    int d = 100;
    int e = 99;
    int f = d - e;
    System.out.println(f);

    // Multiplying
    int g = 20;
    int h = 5; 
    int i = g * h;
    System.out.println(i);

    // Increment and Decrement
    int j = 2;
    j++;
    int k = 4;
    k--;
    int l = i + k;
    System.out.println(l);
    
    // Dividing
    int m = 100;
    int n = 10;
    int o = m / n;
    System.out.println(o);

    // Remainder
    int p = 10;
    int q = 2;
    int r = p % q;
    System.out.println(r);
    
  }
}
